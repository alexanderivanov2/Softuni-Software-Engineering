#Напишете програма, която чете ъгъл в радиани (rad) и го преобразува в градуси) (deg). Потърсете в Интернет подходяща формула.
# Числото π в Python програмите е достъпно чрез math.pi като преди това трябва да реферираме библиотеката math чрез import math.
# Закръглете резултата до най-близкото цяло число използвайки метода round().
import math
rad = float(input())
degrees = round(rad * (180 / math.pi), 0)
print(degrees)
